import {Router} from 'aurelia-router';

export class App {
  static inject() { return [Router]; }
  constructor(router) {
    this.router = router;
    this.router.configure(config => {
      config.title = 'Aurelia';
      config.map([
        { route: ['','layout'],  moduleId: 'layout',      nav: true, title:'Layout Maker' }
      ]);
    });
  }
}